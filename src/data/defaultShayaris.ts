export function getHighlightedText(text: string, highlightedWords: string[]): (string | { word: string; isHighlighted: boolean })[] {
  if (!highlightedWords || highlightedWords.length === 0) return [text];

  // Regex to match any of the words
  const escapedWords = highlightedWords
    .map(w => w.replace(/[-/\\^$*+?.()|[\]{}]/g, '\\$&'))
    .filter(Boolean);

  if (escapedWords.length === 0) return [text];

  const regex = new RegExp(`(${escapedWords.join('|')})`, 'gi');
  const parts = text.split(regex);

  return parts.map(part => {
    const isHighlighted = highlightedWords.some(
      w => w.toLowerCase() === part.toLowerCase()
    );
    return isHighlighted ? { word: part, isHighlighted: true } : part;
  });
}
